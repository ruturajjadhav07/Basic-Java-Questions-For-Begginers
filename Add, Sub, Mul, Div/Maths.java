public class Maths {

    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 10;


        System.out.println(num1 + num1);     //Add
        System.out.println(num1 - num2);     //substract
        System.out.println(num1 * num2);     //multiply
        System.out.println(num1 / num2);     //division
    }

}
